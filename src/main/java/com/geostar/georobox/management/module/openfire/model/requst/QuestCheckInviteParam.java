package com.geostar.georobox.management.module.openfire.model.requst;

public class QuestCheckInviteParam {
	private String roomId;
	private String invitePushMsg;
	private String inspector;
	private String result;
	private String inspectorMark;
	
	public String getRoomId() {
		return roomId;
	}
	public void setRoomId(String roomId) {
		this.roomId = roomId;
	}
	public String getInvitePushMsg() {
		return invitePushMsg;
	}
	public void setInvitePushMsg(String invitePushMsg) {
		this.invitePushMsg = invitePushMsg;
	}
	public String getInspector() {
		return inspector;
	}
	public void setInspector(String inspector) {
		this.inspector = inspector;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getInspectorMark() {
		return inspectorMark;
	}
	public void setInspectorMark(String inspectorMark) {
		this.inspectorMark = inspectorMark;
	}
	

}
