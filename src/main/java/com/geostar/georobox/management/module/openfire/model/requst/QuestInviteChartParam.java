package com.geostar.georobox.management.module.openfire.model.requst;

public class QuestInviteChartParam {
	private String roomId;
	private String invite;
	private String invitees;
	private String inspectorMark;
	
	public String getRoomId() {
		return roomId;
	}
	public void setRoomId(String roomId) {
		this.roomId = roomId;
	}
	public String getInvite() {
		return invite;
	}
	public void setInvite(String invite) {
		this.invite = invite;
	}
	public String getInvitees() {
		return invitees;
	}
	public void setInvitees(String invitees) {
		this.invitees = invitees;
	}
	public String getInspectorMark() {
		return inspectorMark;
	}
	public void setInspectorMark(String inspectorMark) {
		this.inspectorMark = inspectorMark;
	}
	
	
}
